package mx.unam.ciencias.edd.maze;
/**
 * Enumeración para las acciones del programa.
 */
public enum Accion {
    /**
     * Indica que se va a generar un laberinto.
     */
    GENERA,
    /**
     * Indica que se va a resolver un laberinto.
     */
    RESUELVE,
    /**
     * Acción por omisión.
     */
    NINIGUNA;
}
